/**
 * Patches node-pty's native build files for Windows compatibility.
 *
 * Fixes two issues:
 * 1. winpty.gyp uses `cmd /c "cd shared && GetCommitHash.bat"` which fails
 *    because gyp runs from the wrong working directory. We replace the shell
 *    command with a static value.
 * 2. Both binding.gyp and winpty.gyp require Spectre-mitigated libraries
 *    which most dev machines don't have installed. We disable the requirement.
 * 3. winpty.gyp calls UpdateGenVersion.bat which also fails from the wrong
 *    cwd. We pre-create the generated header and point the include to it.
 */

const fs = require('fs')
const path = require('path')

if (process.platform !== 'win32') {
  console.log('[patch-node-pty-win] Not on Windows, skipping.')
  process.exit(0)
}

const nodePtyDir = path.join(__dirname, '..', 'node_modules', 'node-pty')
if (!fs.existsSync(nodePtyDir)) {
  console.log('[patch-node-pty-win] node-pty not found, skipping.')
  process.exit(0)
}

// --- Patch winpty.gyp ---
const winptyGyp = path.join(nodePtyDir, 'deps', 'winpty', 'src', 'winpty.gyp')
if (fs.existsSync(winptyGyp)) {
  let content = fs.readFileSync(winptyGyp, 'utf8')
  // Replace the GetCommitHash.bat call with a static value
  content = content.replace(
    /'WINPTY_COMMIT_HASH%':\s*'<!\(cmd \/c "cd shared && GetCommitHash\.bat"\)'/,
    "'WINPTY_COMMIT_HASH%': 'none'"
  )
  // Replace the UpdateGenVersion.bat call with a static include dir
  content = content.replace(
    /'<!\(cmd \/c "cd shared && UpdateGenVersion\.bat <\(WINPTY_COMMIT_HASH\)"\)'/,
    "'gen'"
  )
  // Disable Spectre mitigation
  content = content.replace(/'SpectreMitigation': 'Spectre'/g, "'SpectreMitigation': 'false'")
  fs.writeFileSync(winptyGyp, content)
  console.log('[patch-node-pty-win] Patched winpty.gyp')

  // Pre-create the GenVersion.h header
  const versionFile = path.join(nodePtyDir, 'deps', 'winpty', 'VERSION.txt')
  const version = fs.existsSync(versionFile)
    ? fs.readFileSync(versionFile, 'utf8').trim()
    : '0.0.0'
  const genDir = path.join(nodePtyDir, 'deps', 'winpty', 'src', 'gen')
  fs.mkdirSync(genDir, { recursive: true })
  fs.writeFileSync(
    path.join(genDir, 'GenVersion.h'),
    `// AUTO-GENERATED BY patch-node-pty-win.js\nconst char GenVersion_Version[] = "${version}";\nconst char GenVersion_Commit[] = "none";\n`
  )
  console.log('[patch-node-pty-win] Created GenVersion.h')
}

// --- Patch binding.gyp ---
const bindingGyp = path.join(nodePtyDir, 'binding.gyp')
if (fs.existsSync(bindingGyp)) {
  let content = fs.readFileSync(bindingGyp, 'utf8')
  content = content.replace(/'SpectreMitigation': 'Spectre'/g, "'SpectreMitigation': 'false'")
  fs.writeFileSync(bindingGyp, content)
  console.log('[patch-node-pty-win] Patched binding.gyp (disabled Spectre mitigation)')
}

console.log('[patch-node-pty-win] Done.')
